function EditableField({ value, onSave }) {
  const [editing, setEditing] = useState(false);
  const [draft, setDraft] = useState(value);

  if (editing) {
    return (
      <div className="flex flex-col gap-1">
        <textarea
          className="w-full bg-slate-800 text-slate-100 border border-slate-500 rounded p-2 text-sm resize-y min-h-16 focus:outline-none focus:border-blue-400"
          value={draft}
          onChange={(e) => setDraft(e.target.value)}
          autoFocus
        />
        <div className="flex gap-2">
          <button
            onClick={() => { onSave(draft); setEditing(false); }}
            className="text-xs bg-blue-600 hover:bg-blue-500 text-white px-3 py-1 rounded"
          >
            Зберегти
          </button>
          <button
            onClick={() => { setDraft(value); setEditing(false); }}
            className="text-xs bg-slate-700 hover:bg-slate-600 text-slate-300 px-3 py-1 rounded"
          >
            Скасувати
          </button>
        </div>
      </div>
    );
  }

  return (
    <div
      className="text-slate-200 text-sm leading-relaxed cursor-pointer hover:bg-slate-700/50 rounded p-1 -m-1 group relative"
      onClick={() => setEditing(true)}
    >
      <span className="whitespace-pre-wrap">{value}</span>
      <span className="ml-2 opacity-0 group-hover:opacity-60 text-xs text-slate-400">✏️</span>
    </div>
  );
}

function BlockCard({ block, onFieldSave }) {
  const [open, setOpen] = useState(true);
  const tagClass = tagColors[block.tag] || "bg-slate-700 text-slate-200";

  return (
    <div className="bg-slate-800 border border-slate-700 rounded-xl overflow-hidden mb-3">
      <button
        onClick={() => setOpen(!open)}
        className="w-full flex items-center gap-3 px-4 py-3 hover:bg-slate-750 text-left"
      >
        <span className={`text-xs font-bold px-2 py-0.5 rounded uppercase tracking-wider ${tagClass}`}>
          {block.tag}
        </span>
        <span className="text-slate-100 font-medium text-sm flex-1">{block.title}</span>
        <span className="text-slate-500 text-xs">{open ? "▲" : "▼"}</span>
      </button>

      {open && (
        <div className="px-4 pb-4 border-t border-slate-700">
          <div className="mt-3 space-y-3">
            {block.fields.map((field, i) => (
              <div key={i} className="grid grid-cols-[180px_1fr] gap-3 items-start">
                <span className="text-slate-500 text-xs pt-1 leading-tight">{field.label}</span>
                <EditableField
                  value={field.value}
                  onSave={(newVal) => onFieldSave(block.id, i, newVal)}
                />
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}

export default function App() {
  const [data, setData] = useState(plan);
  const [activePage, setActivePage] = useState("index");

  const handleFieldSave = (pageId, blockId, fieldIndex, newVal) => {
    setData((prev) => ({
      ...prev,
      pages: prev.pages.map((p) =>
        p.id !== pageId ? p : {
          ...p,
          blocks: p.blocks.map((b) =>
            b.id !== blockId ? b : {
              ...b,
              fields: b.fields.map((f, i) => i !== fieldIndex ? f : { ...f, value: newVal }),
            }
          ),
        }
      ),
    }));
  };

  const currentPage = data.pages.find((p) => p.id === activePage);

  return (
    <div className="min-h-screen bg-slate-900 text-slate-100" style={{ fontFamily: "'IBM Plex Mono', monospace" }}>
      <style>{`@import url('https://fonts.googleapis.com/css2?family=IBM+Plex+Mono:wght@400;500;600&display=swap');`}</style>

      {/* Header */}
      <div className="border-b border-slate-700 bg-slate-900 sticky top-0 z-10">
        <div className="max-w-4xl mx-auto px-4 py-3 flex items-center gap-4">
          <div className="text-slate-400 text-xs uppercase tracking-widest">BTC Consulting</div>
          <div className="text-slate-600 text-xs">/ Контент-план сайту</div>
          <div className="ml-auto flex gap-1">
            {data.pages.map((p) => (
              <button
                key={p.id}
                onClick={() => setActivePage(p.id)}
                className={`text-xs px-3 py-1.5 rounded transition-colors ${
                  activePage === p.id
                    ? "bg-blue-600 text-white"
                    : "bg-slate-800 text-slate-400 hover:text-slate-200"
                }`}
              >
                {p.emoji} {p.label}
              </button>
            ))}
          </div>
        </div>
      </div>

      {/* Content */}
      <div className="max-w-4xl mx-auto px-4 py-6">
        <div className="mb-4 flex items-center gap-3">
          <span className="text-2xl">{currentPage.emoji}</span>
          <div>
            <h1 className="text-lg font-semibold text-slate-100">{currentPage.label}</h1>
            <p className="text-slate-500 text-xs">{currentPage.file} · {currentPage.blocks.length} блоків · Натисніть на текст щоб редагувати</p>
          </div>
        </div>

        {currentPage.blocks.map((block) => (
          <BlockCard
            key={block.id}
            block={block}
            onFieldSave={(blockId, fieldIndex, newVal) =>
              handleFieldSave(activePage, blockId, fieldIndex, newVal)
            }
          />
        ))}
      </div>
    </div>
  );
}
