// ── Main handler ─────────────────────────────────────────────────────────────
export default {
  async fetch(request, env) {
    // Pre-flight
    if (request.method === 'OPTIONS') {
      return new Response(null, { status: 204, headers: CORS });
    }

    if (request.method !== 'POST') {
      return new Response('Method Not Allowed', { status: 405 });
    }

    let body;
    try {
      body = await request.json();
    } catch {
      return json({ error: 'Invalid JSON' }, 400);
    }

    const { messages = [], file } = body;
    if (!messages.length) return json({ error: 'No messages' }, 400);

    // Build Anthropic messages array
    const anthropicMessages = messages.map((m, i) => {
      // If a file is attached and this is the last user message, prepend the document
      if (file && i === messages.length - 1 && m.role === 'user') {
        const content = [];
        if (file.type === 'application/pdf' || file.type?.startsWith('image/')) {
          content.push({
            type: file.type.startsWith('image/') ? 'image' : 'document',
            source: { type: 'base64', media_type: file.type, data: file.data },
          });
        }
        content.push({ type: 'text', text: m.content });
        return { role: 'user', content };
      }
      return { role: m.role, content: m.content };
    });

    // Anthropic API call — streaming
    const apiResp = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'x-api-key': env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01',
        'anthropic-beta': 'pdfs-2024-09-25',
        'content-type': 'application/json',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 1024,
        system: SYSTEM_PROMPT,
        messages: anthropicMessages,
        stream: true,
      }),
    });

    if (!apiResp.ok) {
      const err = await apiResp.text();
      return json({ error: err }, apiResp.status);
    }

    // Pass SSE stream straight through
    return new Response(apiResp.body, {
      status: 200,
      headers: {
        'content-type': 'text/event-stream; charset=utf-8',
        'cache-control': 'no-cache',
        ...CORS,
      },
    });
  },
};

function json(data, status = 200) {
  return new Response(JSON.stringify(data), {
    status,
    headers: { 'content-type': 'application/json', ...CORS },
  });
}
